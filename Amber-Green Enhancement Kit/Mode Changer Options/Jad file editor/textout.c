/*
 * Name:
 *     OutGLString
 * Description:
 *     Prints a String, in FONT Number.
 * Input:
 *     sx, sy:    Position in Screen-Coordinates
 *     text:      Text to print.
 *     textcolor: Color of text  ( RGBA )
 *     fontno:	   Number of font to write text with
 */

void OutGLString(int sx, int sy, char *text, int textcolor, int fontno)
{
    FONT *fptr;
    GLubyte *ptext;
    int  cx, cy;
    GLubyte c;

    /* first test for NULL-Pointer */
    if (text == 0)
        return;

    glPixelStorei(GL_UNPACK_ALIGNMENT, 1);
    glColor3ubv(fontcolors[textcolor & 0x0F]);
    // glColor3f(1.0,1.0,1.0);
    /* Because OpenGL draws the bitmaps form bottom to top, we have to  */
    /* add the heigth of the font to the position			*/

    if ((fontno < 0) || (fontno > 5))
        fontno = 0;			/* Just to be save		*/

    fptr = &_wFonts[fontno];

    sy += fptr -> fonth;

    cx = sx;
    cy = sy;				/* Save for Scrolling 		*/

    glRasterPos2i(cx, cy);		/* Set Start-Position for draw	*/

    /****** Put Text itself ***********************************/

    ptext = (GLubyte *)text;

    while (*ptext != 0) {     /* Get char */
        c = (char)(*ptext & 0x7F);  /* Max. 127 */
        switch (c) {
            case '\r':
		/* Make CR */
                cy += fptr -> fonth;	/* Next line downwards */
                cx = sx;		/* Start of next line  */
                glRasterPos2i(cx, cy);
                break;
            case 6:
            	/* Set Color */
		ptext++;
		glColor3ubv(fontcolors[*ptext]);
                glRasterPos2i(cx, cy);
                break;
            case '\n':
                /* Ignore Carriage return */
                break;
            default:
		glBitmap(fptr -> cwidth, fptr -> fonth,
                0, 0,
                fptr -> fontw, 0,
                &fptr -> font[c * fptr -> fonth]);
        }

        ptext++;		/* next char */
        cx += fptr -> fontw;	/* hold charpos for Carriage-Return */
    }
}


/*
 * Name:
 *     OutGLchar
 * Description:
 *     Output of one single char.
 * Input:
 *     sx, sy: Position where the char to put
 *     c:      The char to put out
 *     textcolor: Color of char
 *     fontno: Number of font to write with 		
 */

void OutGLchar(int sx, int sy, char c, int textcolor, int fontno)
{
    FONT * fptr;

    fptr = &_wFonts[fontno];

    glPixelStorei(GL_UNPACK_ALIGNMENT, 1);
    glColor3ubv(fontcolors[textcolor]);
    glRasterPos2i(sx, sy);
    glBitmap(fptr -> cwidth, fptr -> fonth,
             0, 0,
             fptr -> fontw, 0,
             &fptr -> font[c * fptr -> fonth]);
}

