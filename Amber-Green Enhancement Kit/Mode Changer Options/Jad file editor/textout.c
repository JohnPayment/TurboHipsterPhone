/*
 * Name:
 *     SetFont
 * Function:
 *     Sets a font for "TextOut"
 * Input:
 *     font: Font-Bitmap(s)
 *     fontw: Width of font
 *     fonth: Height of font	
 */

void SetFont(unsigned char * font, int fontw, int fonth, int fontno)
{
    FONT *fptr;

    if (fontno < 0 ||fontno > 5)
    	fontno = 2;
        
    fptr = &_wFonts[fontno];

    fptr -> font = font;
    fptr -> fontw = fontw;
    fptr -> fonth = fonth;
}

/*
 * Name:
 *     FontGetSize
 * Function:
 *     Gives back the size of the asked font
 * Input:
 *     Number of font
 * Output:
 *    int: loword(fontwidth) / hiword(fontheight)
 */

int FontGetSize(int fontno)
{
   return ((_wFonts[fontno].fontw & 0xFFFF) + (_wFonts[fontno].fonth << 16));
}
